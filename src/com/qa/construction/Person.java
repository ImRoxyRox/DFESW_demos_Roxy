package com.qa.construction;

public class Person {
	public String name;
	public String status;
	public int age;
	public String hairColour;
	
	public Person(String name, String status, int age, String hairColour) {
		this.name = name;
		this.status = status;
		this.age = age;
		this.hairColour = hairColour;
	}
	public Person(String name, String status, String hairColour) {
		this.name = name;
		this.status = status;
		this.hairColour = hairColour;
	}

}
